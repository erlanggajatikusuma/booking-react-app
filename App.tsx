function App(props: NavigationProps): JSX.Element {
  return (
    <StoreProvider>
      <SafeAreaProvider>
        <NavigationContainer theme={DefaultTheme} {...props}>
          <Stack.Navigator initialRouteName="App.Stack">
            <Stack.Screen name="App.Stack" options={{headerShown: false}}>
              {AppNavigator}
            </Stack.Screen>
          </Stack.Navigator>
        </NavigationContainer>
      </SafeAreaProvider>
    </StoreProvider>
  );
}
const styles = StyleSheet.create({
  sectionContainer: {
    marginTop: 32,
    paddingHorizontal: 24,
  },
  sectionTitle: {
    fontSize: 24,
    fontWeight: '600',
  },
  sectionDescription: {
    marginTop: 8,
    fontSize: 18,
    fontWeight: '400',
  },
  highlight: {
    fontWeight: '700',
  },
});

export default App;
